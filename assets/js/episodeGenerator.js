function generateNumber() {
    var episode = Math.floor(Math.random() * Object.keys(episodes).length) + 1;
    var episodeInfo = episodes[episode];

    if (episodeInfo) {
        var title = episodeInfo[0];
        var releaseDate = episodeInfo[1];

        var episodeContent = document.getElementById("episodeContent");
        episodeContent.textContent = "Episode " + episode + ": " + title + " (" + releaseDate + ")";
    } else {
        var episodeContent = document.getElementById("episodeContent");
        episodeContent.textContent = "Episode information not found. Please try again.";
    }
}

// Call the function when the page loads
window.onload = function () {
    generateNumber();
};